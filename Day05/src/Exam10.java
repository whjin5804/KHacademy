
public class Exam10 {
	public static void main(String[] args) {
		int[] arr = {1,2 ,3 ,4 ,5 };
		for (int i = 0; i < arr.length; i++) {
			System.out.print(arr[i] + " ");
		}
		for (int i : arr) {
			System.out.println(i);
		}
		
		System.out.println("天天天天");
		double[] arr1 = {1.1, 2.2, 3.3};
		
		for (double e : arr1) {
			System.out.println(e);
		}
		
		
	}
}
