package pac1;

public class Person {
	private String name;
	private int age;
	private String address;
	
	public Person() {
		name = "";
		age = 0;
		address = "";
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public void setAge(int age) {
		this.age = age;
	}
	
	public void setAddress(String address) {
		this.address = address;
	}
	
	
}
